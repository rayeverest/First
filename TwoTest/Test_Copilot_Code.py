# Python program to print the number of seconds in a year

# Number of seconds in a minute
seconds_per_minute = 60

# Number of minutes in an hour
minutes_per_hour = 60

# Number of hours in a day
hours_per_day = 24

# Number of days in a year (non-leap year)
days_per_year = 365

# Calculate the number of seconds in a year
seconds_per_year = seconds_per_minute * minutes_per_hour * hours_per_day * days_per_year

# Print the result
print(f"The number of seconds in a year is: {seconds_per_year}")